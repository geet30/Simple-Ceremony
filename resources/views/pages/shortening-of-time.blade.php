            <h2 class="h2 text-center netural-100 pb-100">Below is a list of circumstances in which the application may be granted by a Registrar:</h2>
         </div>
         <div class="col-md-12">
            <div class="registrar-circumstances-list-description">
               <h4 class="h4">1. Employment-related or other travel commitments</h4>
               <ul class="list-unstyled mb-0 ps-3">
                  <li class="d-flex align-items-start mt-4">
                     <img style="top:2px;" class="me-3 position-relative" src="/images/green-success.svg" alt="Check Icon">
                     <p class="body-3-regular mb-0 netural-100">The marriage should be solemnised despite the required notice not having been received in time because a party to the marriage or someone involved with the proposed wedding :</p>
                  </li>
               </ul>
               <ul class="list-unstyled mb-0 ps-5">
                  <li class="mt-4"><p class="body-3-regular mt-4 mb-0 netural-100"><span class="body-2">A.</span> Has employment commitments that require the party's absence from the location of the proposed wedding for a considerable period of time</p></li>
                  <li class="mt-4"><p class="body-3-regular mt-4 mb-0 netural-100"><span class="body-2">B.</span> has other travel commitments.</p></li>
               </ul>
               <div class="details-examples mt-4 mb-4">
                  <p class="body-1 netural-100">Example 1 : </p>
                  <p class="body-3-regular mb-0 netural-100">A party to the marriage has accepted an offer of employment for imminent transfer or posting overseas or to a part of Australia distant from the location of the proposed wedding for at least 3 months, and wishes to be married with the party's family and friends present before the departure.</p>
               </div>
               <div class="details-examples mt-4 mb-4">
                  <p class="body-1 netural-100">Example 2 : </p>
                  <p class="body-3-regular mb-0 netural-100">A party to the marriage realises that a close relative or friend of the party is in Australia but the relative or friend has a non-redeemable ticket for departure from Australia within less than a month, and the party wishes the relative or friend to be present at the wedding.</p>
               </div>
               <ul class="list-unstyled mb-0 ps-3">
                  <li class="d-flex align-items-start mt-4">
                     <img style="top:2px;" class="me-3 position-relative" src="/images/green-success.svg" alt="Check Icon">
                     <p class="body-3-regular mb-0 netural-100">In determining whether a circumstance in subclause (1) is met, the prescribed authority may take into account the following :</p>
                  </li>
               </ul>
               <ul class="list-unstyled mb-0 ps-5">
                  <li class="mt-4"><p class="body-3-regular mt-4 mb-0 netural-100"><span class="body-2">A.</span> Documents relating to the employment commitments of a party to the marriage such as a letter of offer and a letter of acceptance.</p></li>
                  <li class="mt-4"><p class="body-3-regular mt-4 mb-0 netural-100"><span class="body-2">B.</span> Documents relating to the travel of a person such as a dated receipt or a ticket.</p></li>
                  <li class="mt-4"><p class="body-3-regular mt-4 mb-0 netural-100"><span class="body-2">C.</span> Any explanation provided for not giving the notice sooner.</p></li>
                  <li class="mt-4"><p class="body-3-regular mt-4 mb-0 netural-100"><span class="body-2">D.</span> Any explanation provided for not postponing the proposed wedding.</p></li>
                  <li class="mt-4"><p class="body-3-regular mt-4 mb-0 netural-100"><span class="body-2">E.</span> Whether hardship would be caused to a party to the marriage if the marriage is not solemnised as proposed.</p></li>
                  <li class="mt-4"><p class="body-3-regular mt-4 mb-0 netural-100"><span class="body-2">F.</span> Any other matter that the prescribed authority considers relevant.</p></li>
               </ul>
            </div>
